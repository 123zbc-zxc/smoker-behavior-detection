STYLE_OVERRIDES: dict[int, str] = {
    211: "Heading 2",
}


def find_target_docx() -> Path:
    matches = [p for p in ROOT.glob("定稿_智能224-*.docx") if not p.name.startswith("~$")]
    if not matches:
        raise FileNotFoundError("未找到当前论文 DOCX 文件。")
    return matches[0]


def backup_docx(target: Path) -> None:
    BACKUP_PATH.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(target, BACKUP_PATH)


def rewrite_doc(doc_path: Path) -> None:
    doc = Document(doc_path)
    for idx, text in REPLACEMENTS.items():
        if idx >= len(doc.paragraphs):
            raise IndexError(f"Paragraph index out of range: {idx}")
        doc.paragraphs[idx].text = text
        if idx in STYLE_OVERRIDES:
            doc.paragraphs[idx].style = STYLE_OVERRIDES[idx]
    doc.save(doc_path)


def main() -> None:
    target = find_target_docx()
    backup_docx(target)
    rewrite_doc(target)
    if ALIGNED_COPY_PATH.exists():
        shutil.copy2(target, ALIGNED_COPY_PATH)
    print(f"Backup saved to: {BACKUP_PATH}")
    print(f"Rewritten thesis saved to: {target}")
    if ALIGNED_COPY_PATH.exists():
        print(f"Aligned copy synced to: {ALIGNED_COPY_PATH}")


if __name__ == "__main__":
    main()
